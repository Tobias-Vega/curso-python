# Operaciones con números
x = 5
y = 2
suma = x + y         # 7
resta = x - y        # 3
producto = x * y     # 10
division = x / y     # 2.5
division_entera = x // y  # 2
modulo = x % y       # 1
potencia = x ** y    # 25

# Operaciones de cadenas
nombre = "Juan"
saludo = "Hola, " + nombre + "!"
print(saludo)  # Imprime: Hola, Juan!

# Conversión de tipos
numero_str = "123"
numero_int = int(numero_str)  # Convierte a entero: 123